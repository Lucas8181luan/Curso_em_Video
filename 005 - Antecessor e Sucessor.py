# UMA PROGRAMAR QUE LEIA UM NÚMERO, E MOSTRE SEU ANTECESSOR E SUCESSOR

# 1° Passo, Entrada do Número
numero = int(input("Digite um Número = "))

# Seu Antecessor
antecessor = numero - 1

# Seu Sucessor
sucessor = numero + 1

print("Analisando o Valor {} seu Antecessor e {} e seu Sucessor e {}".format(numero, antecessor, sucessor))
