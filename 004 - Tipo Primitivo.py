# COMO INFORMAR O TIPO PRIMITIVO DE QUAL QUE COISA ESCRITA NO TECLADO 

# 1° Passo, Crie a Entrada do Usuario
entrada = input("Digite Algo = ")

# 2° Passo, Exibar as Informações
print("O tipo primitivo desse valor e = ", type(entrada))

# Verificar se um número
print("E número? ", entrada.isnumeric())

# Verificar se Alfanumérico
print("E alfanumérico? ", entrada.isalnum())

# Verificar se Alfabético
print("E alfabético? ", entrada.isalpha())

