# UM PROGRAMA QUE CONVERTA UMA TEMPERATURA DIGITADA EM °C PARA °F

# 1 Passo, Entrada para digitar a temperatura
temperatura = float(input("Informe a temperatura = "))

# Faça a soma
soma_1 = (9 * temperatura)
soma_2 = soma_1 / 5
soma_final = soma_2 + 32

print("A temperatura de {}°C e igual a = {}°F".format(temperatura, soma_final))

