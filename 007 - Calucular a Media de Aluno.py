# MOSTRA DUAS NOTAS DE UM ALUNO E CALCULAR A SUA MÉDIA

# 1° Passo, Ler As Duas Notas
nota_1 = float(input("Primeira Nota = "))

nota_2 = float(input("Ler a Segunda Nota = "))

# 2° Passo, realizar a soma
soma = (nota_1 + nota_2) / 2

print("A média entre {:.1f} e {:.1f} é igual a = {:.1f}".format(nota_1, nota_2, soma))



